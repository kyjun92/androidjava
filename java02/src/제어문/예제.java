package 제어문;

public class 예제 {
	public static void main(String[] args) {
		int x = 0;
		double y = x; // 정수인 x의 값이 double인 y의 자료형으로 자동형변환
					// int의 데이터 크기가 double보다 작기 때문에 double변환이 가능
		
		double x1 = 100;
		int y1 = x;
		
		
		int x2 = 100;
		byte y2 = (byte)x2; // 데이터 크기가 큰 것을 작은 것으로 변환할 때 사용
		
		
	}
}
